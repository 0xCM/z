namespace Z0
{
    [SymSource(chars)]
    public enum AsciMathSym : ushort
    {
        /// <summary>
        /// The '=' character
        /// </summary>
        Eq = '=',

        /// <summary>
        /// The '>' symbol
        /// </summary>
        GT = '>',

        /// <summary>
        /// The ',' symbol
        /// </summary>
        LT = '<',

        /// <summary>
        /// The '^' character
        /// </summary>
        Exp = '^',

        /// <summary>
        /// The '+' character
        /// </summary>
        Add = '+',

        /// <summary>
        /// The '-' character
        /// </summary>
        Sub = '-',

        /// <summary>
        /// The '*' character
        /// </summary>
        Mul = '*',

        /// <summary>
        /// The '/' symbol
        /// </summary>
        Div = '/',

        /// <summary>
        /// The '%' character
        /// </summary>
        Mod = '%',

        /// <summary>
        /// The '~' character
        /// </summary>
        Complement = '~',

        /// <summary>
        /// The '!' character
        /// </summary>
        Not = '!',

        /// <summary>
        /// The '&' character
        /// </summary>
        And = '&',

        /// <summary>
        /// The '|' character
        /// </summary>
        Or = '|',

        /// <summary>
        /// The '^' character
        /// </summary>
        Xor = '^',
    }
}