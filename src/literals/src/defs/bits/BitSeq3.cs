namespace Z0
{
    /// <summary>
    /// Defines literals corresponding to the set of unique 3-bit sequences
    /// </summary>
    [SymSource("bitseq")]
    public enum BitSeq3 : byte
    {
        /// <summary>
        /// 0
        /// </summary>
        b000 = 0b000,

        /// <summary>
        /// 1
        /// </summary>
        b001 = 0b001,

        /// <summary>
        /// 2
        /// </summary>
        b010 = 0b010,

        /// <summary>
        /// 3
        /// </summary>
        b011 = 0b011,

        /// <summary>
        /// 4
        /// </summary>
        b100 = 0b100,

        /// <summary>
        /// 5
        /// </summary>
        b101 = 0b101,

        /// <summary>
        /// 6
        /// </summary>
        b110 = 0b110,

        /// <summary>
        /// 7
        /// </summary>
        b111 = 0b111,
    }
}