        [MethodImpl(Inline), Op]
        public static uint copy(ReadOnlySpan<byte> src, ref byte dst)
        {
            var size = (uint)src.Length;
            for(var i=0; i<size; i++)
                seek(dst,i) = skip(src,i);
            return size;
        }

