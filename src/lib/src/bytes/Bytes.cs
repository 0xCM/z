        [MethodImpl(Inline), Op]
        public static uint join(byte a, byte b, byte c, byte d)
        {
            var au = (uint)a;
            var bu = (uint)b << 8;
            var cu = (uint)c << 16;
            var du = (uint)d << 24;
            return au | bu | cu | du;
        }

