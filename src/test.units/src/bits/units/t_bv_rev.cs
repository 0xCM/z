        protected void bvrev_natcheck<N,T>()
            where N : unmanaged, ITypeNat
            where T : unmanaged
        {
            for(var i=0; i< RepCount; i++)
            {
                var x = Random.ScalarBits<N,T>();
                var y = ScalarBits.reverse(x);
                var z = x.ToBitString().Reverse().ToScalarBits<N,T>();
                Claim.eq(y,z);
            }
        }
