    XA = 0b01010,

    XB = 0b01011,

    XC = 0b01100,

    XD = 0b01101,

    XE = 0b01110,

    XF = 0b01111,

    XG = 0b10000,

    XH = 0b10001,

    XI = 0b10010,

    XJ = 0b10011,

    XK = 0b10100,

    XL = 0b10101,

    XM = 0b10110,

    XN = 0b10111,

    XO = 0b11000,

    XP = 0b11001,

    XQ = 0b11010,

    XR = 0b11011,

    XS = 0b11100,

    XT = 0b11101,

    XU = 0b11110,

    XV = 0b11111,
}
