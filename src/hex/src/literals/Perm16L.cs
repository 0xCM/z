namespace Z0;

/// <summary>
/// Defines canonical literals for representing terms of permutations on 16 symbols
/// </summary>
public enum Perm16L : ulong
{
    X0 = 0b0000,

    X1 = 0b0001,

    X2 = 0b0010,

    X3 = 0b0011,

    X4 = 0b0100,

    X5 = 0b0101,

    X6 = 0b0110,

    X7 = 0b0111,

    X8 = 0b1000,

    X9 = 0b1001,

    XA = 0b1010,

    XB = 0b1011,

    XC = 0b1100,

    XD = 0b1101,

    XE = 0b1110,

    XF = 0b1111,
}
