namespace Z0
{
    /// <summary>
    /// Defines literals that correspond to the base-16 digits
    /// </summary>
    [SymSource(hex_digits, NBK.Base16)]
    public enum HexDigitValue : byte
    {
        /// <summary>
        /// Specifies 0 base 16
        /// </summary>
        x0 = 0x0,

        /// <summary>
        /// Specifies 1 base 16
        /// </summary>
        x1 = 0x1,

        /// <summary>
        /// Specifies 2 base 16
        /// </summary>
        x2 = 0x2,

        /// <summary>
        /// Specifies 3 base 16
        /// </summary>
        x3 = 0x3,

        /// <summary>
        /// Specifies 4 base 16
        /// </summary>
        x4 = 0x4,

        /// <summary>
        /// Specifies 5 base 16
        /// </summary>
        x5 = 0x5,

        /// <summary>
        /// Specifies 6 base 16
        /// </summary>
        x6 = 0x6,

        /// <summary>
        /// Specifies 7 base 16
        /// </summary>
        x7 = 0x7,

        /// <summary>
        /// Specifies 8 base 16
        /// </summary>
        x8 = 0x8,

        /// <summary>
        /// Specifies 9 base 16
        /// </summary>
        x9 = 0x9,

        /// <summary>
        /// Specifies 10 base 16
        /// </summary>
        A = 0xA,

        /// <summary>
        /// Specifies 11 base 16
        /// </summary>
        B = 0xB,

        /// <summary>
        /// Specifies 12 base 16
        /// </summary>
        C = 0xC,

        /// <summary>
        /// Specifies 13 base 16
        /// </summary>
        D = 0xD,

        /// <summary>
        /// Specifies 14 base 16
        /// </summary>
        E = 0xE,

        /// <summary>
        /// Specifies 15 base 16
        /// </summary>
        F = 0xF,
    }
}